    private void TFSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TFSearchKeyReleased
        try {
            if(TFSearch.getText().equals("")){
                FillYourBalls();
            }else{
                String Query = "SELECT fur_id AS ID, fur_type AS Type , fur_price AS Price, fur_length AS Length, fur_width AS Width, fur_height AS Height, fur_lengthl AS LengthL, fur_widthl AS WidthL, fur_heightl AS HeightL FROM table_furniture WHERE fur_type LIKE '%"+ TFSearch.getText() +"%'";
                PreparedStatement PreStat = Con.prepareStatement(Query);
                ReS = PreStat.executeQuery();
                Table.setModel(DbUtils.resultSetToTableModel(ReS));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmFur.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_TFSearchKeyReleased

