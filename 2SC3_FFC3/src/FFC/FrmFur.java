    private void TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMouseClicked
        try {
            DefaultTableModel Model = (DefaultTableModel) Table.getModel();
            PreparedStatement PreStat = Con.prepareStatement("SELECT * FROM table_furniture WHERE fur_id = "+ Model.getValueAt(Table.getSelectedRow(), 0) +"");
            ResultSet ReSA = PreStat.executeQuery();
            if(ReSA.next()){
                TFID.setText(Integer.toString(ReSA.getInt("fur_id")));
                TFName.setText(ReSA.getString("fur_type"));
                TFLength.setText(Integer.toString(ReSA.getInt("fur_length")));
                TFWidth.setText(Integer.toString(ReSA.getInt("fur_width")));
                TFHeight.setText(Integer.toString(ReSA.getInt("fur_height")));
                TFLengthL.setText(Integer.toString(ReSA.getInt("fur_lengthl")));
                TFWidthL.setText(Integer.toString(ReSA.getInt("fur_widthl")));
                TFHeightL.setText(Integer.toString(ReSA.getInt("fur_heightl")));
                TFPrice.setText(Integer.toString(ReSA.getInt("fur_price")));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmFur.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_TableMouseClicked

    private void ButAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ButAddMouseClicked
        if(TFName.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Please fill the furniture name field !");
        }else{
            GetName = TFName.getText();
            GetLength = Integer.parseInt(TFLength.getText());
            GetWidth = Integer.parseInt(TFWidth.getText());
            GetHeight = Integer.parseInt(TFHeight.getText());
            GetLengthL = Integer.parseInt(TFLengthL.getText());
            GetWidthL = Integer.parseInt(TFWidthL.getText());
            GetHeightL = Integer.parseInt(TFHeightL.getText());
            GetPrice = Integer.parseInt(TFPrice.getText());
            Func.AddFur(GetName, GetLength, GetWidth, GetHeight, GetLengthL, GetWidthL, GetHeightL, GetPrice);
            FillYourBalls();
        }
    }//GEN-LAST:event_ButAddMouseClicked

